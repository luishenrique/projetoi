    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (horasTrabalhadasText.getText().isEmpty()
                || diasTrabalhadosText.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Obrigatório o preenchimento de todos os campos.", "Atenção", JOptionPane.WARNING_MESSAGE);

        }

        int dias = Integer.parseInt(diasTrabalhadosText.getText());
        double horas = Double.parseDouble(horasTrabalhadasText.getText());

        //calculo disponibilidade
        double resultadoDisponilidade = dias * horas;

        //chamando classe DecimalFomart e configurando com 2 digitos as casa decimais
        DecimalFormat myformat = new DecimalFormat();
        myformat.setMaximumFractionDigits(2);
        myformat.setMinimumFractionDigits(2);

        resultadoDisponibilidadeText.setText(String.valueOf(resultadoDisponilidade));

        //so calcula disponibilidade ser conter algum conteudo no carregamento total
        if (!(resultadoCarregamentoText.getText()).isEmpty()) {

            //soma total carregamento
            int qtdeLinhas = jTabela.getRowCount();
            Double soma = 0.0;
            for (int i = 0; i < qtdeLinhas; i++) {
                soma += (Double) jTabela.getValueAt(i, 5);
            }

            String somaNova = myformat.format(soma);
            resultadoCarregamentoText.setText(somaNova);
            double resultadoTotal = soma;

            //verificar disponibilidade
            double horasExtrasResult = Math.abs(resultadoDisponilidade - resultadoTotal);
            double horasExtrasResultDias = horasExtrasResult / dias;

            //formataçao casa decimais
            String horasExtrasFormat = myformat.format(horasExtrasResult);
            String horasExtrasDiasFormat = myformat.format(horasExtrasResultDias);

            //icons
            ImageIcon iconAutorizado = new ImageIcon(getClass().getResource("autorizado.png"));
            ImageIcon iconNaoAutorizado = new ImageIcon(getClass().getResource("naoautorizado.png"));

            //disponibilidade
            if (resultadoTotal <= resultadoDisponilidade) {
                mensagemProducaoText.setIcon(iconAutorizado);
                horasExtraText.setText("0");
                horasExtraDiaText.setText("0");
            } else {
                mensagemProducaoText.setIcon(iconNaoAutorizado);
                JOptionPane.showMessageDialog(null, "Carregamento não suportado. \nSão necessárias: " + horasExtrasFormat + " horas extras.", "Atenção", JOptionPane.WARNING_MESSAGE);
                horasExtraText.setText(horasExtrasFormat);
                horasExtraDiaText.setText(horasExtrasDiasFormat);
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed


    private void jMenu2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenu2MouseClicked
        int resposta;
        resposta = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja resetar o sistema? "
                + "\nVocê ira perder todos os dados!", "Reiniciar Sistema", JOptionPane.YES_NO_OPTION);
        if (resposta == JOptionPane.YES_OPTION) {

            //limpando campos
            codigoText.setText("1");
            produtoText.setText("");
            taxaProducaoText.setText("");
            setupText.setText("");
            previsaoText.setText("");
            resultadoCarregamentoText.setText("0");
            resultadoDisponibilidadeText.setText("0");
            jComboBox1.setSelectedIndex(0);
            diasTrabalhadosText.setText("");
            horasTrabalhadasText.setText("");
            mensagemProducaoText.setIcon(null);
            horasExtraText.setText("");
            horasExtraDiaText.setText("");

            //limpando tabela
            DefaultTableModel remover = (DefaultTableModel) jTabela.getModel();
            if (jTabela.getRowCount() >= 0) {
                int numeroLinhas = jTabela.getRowCount();
                for (int i = numeroLinhas; i > 0; i--) {
                    remover.removeRow(0);
                }
            }
        }
    }//GEN-LAST:event_jMenu2MouseClicked

